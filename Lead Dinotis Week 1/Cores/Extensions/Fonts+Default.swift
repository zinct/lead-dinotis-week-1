import SwiftUI

extension Font {
    static func robotoBlack(size: CGFloat) -> Font {
        return .custom("Roboto-Black", size: size)
    }
    
    static func robotoBold(size: CGFloat) -> Font {
        return .custom("Roboto-Bold", size: size)
    }
    
    static func robotoLight(size: CGFloat) -> Font {
        return .custom("Roboto-Light", size: size)
    }
    
    static func robotoMedium(size: CGFloat) -> Font {
        return .custom("Roboto-Medium", size: size)
    }
    
    static func robotoRegular(size: CGFloat) -> Font {
        return .custom("Roboto-Regular", size: size)
    }
    
    static func robotoThin(size: CGFloat) -> Font {
        return .custom("Roboto-Thin", size: size)
    }
}

